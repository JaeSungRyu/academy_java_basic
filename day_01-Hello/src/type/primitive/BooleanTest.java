package type.primitive;


/**
 * 자바의 유일한 논리형 값 : boolean
 * 저장될 수 있는 값 : true / false
 * boolean 타입을 테스트 해보는 클래스이다.
 * 
 */
public class BooleanTest {

	public static void main(String[] args) {
		//1.선언 : 타입 변수이름;
		boolean flag; //지역변수
		
		//2.초기화 : true 값 할당
		flag = true;
		
		//3.사용 : flag 변수의 값을 출력
		System.out.println("boolean flag : " + flag);
	}

}


